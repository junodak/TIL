N = int(input())

print('*'*N + ' '*(N*2-3) + '*'*N)

for i in range(1,N-1):
    print(' '*i + '*' + ' '*(N-2) + '*' + ' '*(N*2-3-2*i) + '*' + ' '*(N-2) + '*')

print((' '*(N-1) + '*') + (' '*(N-2) + '*')*2)

for i in range(1,N-1):
    print(' '*(N-i-1) + '*' + ' '*(N-2) + '*' + ' '*(2*i-1) + '*' + ' '*(N-2) + '*')

print('*'*N + ' '*(N*2-3) + '*'*N)
