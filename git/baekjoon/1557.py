def primes(n):
    arr = [1] * (n + 1)
    arr[0] = arr[1] = 0
    for i in range(4, n+1, 2):
        arr[i] = 0
    for i in range(3, int(n**0.5) + 1):
        if arr[i]:
            for j in range(i*i, n+1, i*2):
                arr[j] = 0
    return [i**2 for i in range(n+1) if arr[i]]

# n = 10억, 2*30  // 10억번째 : 16억~17억, 제곱수 4300개 미만
n = int(input())
arr = primes(int((n)**0.5))
print(arr)
print(len(arr))



